day = input("Day : ")
day = day.lower()
if(day == "sunday"):
	print("1")
elif(day == "monday"):
	print("2")
elif(day == "tuesday"):
	print("3")
elif(day == "wednesday"):
	print("4")
elif(day == "thursday"):
	print("5")
elif(day == "friday"):
	print("6")
elif(day == "saturday"):
	print("7")
else:
	print("Invalid")
